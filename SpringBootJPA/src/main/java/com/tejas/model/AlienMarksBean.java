package com.tejas.model;

public class AlienMarksBean {
	
	private Integer alienId;
	private String alienName;
	private String alienTechnology;
	private Marks marks;
	
	public Integer getAlienId() {
		return alienId;
	}
	public void setAlienId(Integer alienId) {
		this.alienId = alienId;
	}
	public String getAlienName() {
		return alienName;
	}
	public void setAlienName(String alienName) {
		this.alienName = alienName;
	}
	public String getAlienTechnology() {
		return alienTechnology;
	}
	public void setAlienTechnology(String alienTechnology) {
		this.alienTechnology = alienTechnology;
	}
	public Marks getMarks() {
		return marks;
	}
	public void setMarks(Marks marks) {
		this.marks = marks;
	}
	
	
	
	
}
